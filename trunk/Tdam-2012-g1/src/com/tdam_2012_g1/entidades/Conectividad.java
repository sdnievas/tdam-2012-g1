package com.tdam_2012_g1.entidades;

public class Conectividad {
	int _id;
	String _conexion;
	String _estado;
	String _fecha;
	
	public Conectividad() {		
	}
	
	public int get_id() {
		return _id;
	}
	public void set_id(int _id) {
		this._id = _id;
	}
	public String get_conexion() {
		return _conexion;
	}
	public void set_conexion(String _conexion) {
		this._conexion = _conexion;
	}
	public String get_estado() {
		return _estado;
	}
	public void set_estado(String _estado) {
		this._estado = _estado;
	}
	public String get_fecha() {
		return _fecha;
	}
	public void set_fecha(String _fecha) {
		this._fecha = _fecha;
	}
	
	
	

}
